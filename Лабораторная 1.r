mnk = function(n, ar) {
   	sum_1 = 0
   	sum_2 = 0
   	for(i in 2 : n) {
     	sum_1 = sum_1 + (ar[i-1] * ar[i-1])
     	sum_2 = sum_2 + (ar[i-1] * ar[i])
   	}
  	return (sum_2/sum_1)
}

