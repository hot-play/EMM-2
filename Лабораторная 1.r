ar2 = function(n, O1, O2) {
    x = double(n)
    x[1] = 1
    x[2] = 2
    for (i in 3 : n) {
       	x[i] = O1 * x[i - 2] + O2 * x[i - 1] + rnorm(1, 0, 0.5)
    }
    return(x)
}
 
