mnk = function(g, n) {
    M = double(2)
    x = 0
    y = 0
    xx = 0
    xy = 0
    for (i in 2 : n) {
        x = x + (g[i-1] ** 2)
        y = y + (g[i] ** 2)
        xx = xx + (g[i-1] ** 4)
        xy = xy + (g[i-1] ** 2) * (g[i] ** 2)
    }
    M[1] = (xy / n-x * y / (n ** 2)) / (xx / n - (x / n) ** 2)
    M[2] = (y / n) - (M[1] * x / n)
    return (M)
}
