garch1 = function(a0, a1, n) {
    d = double(n)
    h = double(n)
    h[1] = 0
    for(i in 2 : n + 1){
        d[i] = a0 + a1 * h[i-1] ** 2
        h[i] = rnorm(1, 0, 1) * sqrt(d[i])
    }
    plot(h, type='l')
    plot(d, type='l')
    return(h)
}
