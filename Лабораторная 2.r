prognoz = function(a, g) {
    n = length(g)
    s = double(n)
    s[1] = a[1]
    s[2] = a[1] + a[2] * g[1] ** 2
    s[3] = a[1] + a[2] * g[2] ** 2 + a[3] * g[1] ** 2
    for(i in 4:n) {
        s[i] = a[1] + a[2] * g[i-1] ** 2 + a[3] * g[i-2] ** 2 + g[4] * g[i-3] ** 2
    }
    plot(g*g, type='l', col = "black")
    lines(s, type='p', col = "red")
    return(s)
}
